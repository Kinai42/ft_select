#include "libft.h"

int	ft_isnumber(char *s)
{
	int i;

	i = *s == '-' ? 1 : 0;
	if (!s[i])
		return (0);
	while (s[i])
		if (!ft_isdigit((int)s[i++]))
			return (0);
	return (1);
}
