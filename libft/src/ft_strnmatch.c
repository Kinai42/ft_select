#include "libft.h"

char	ft_strmatch(char *s1, char *s2)
{
	int i;

	i = -1;
	while (s1[++i] && ft_strrchr(s2, s1[i]))
		;
	if (s1[i])
		return (s1[i]);
	return (0);
}
